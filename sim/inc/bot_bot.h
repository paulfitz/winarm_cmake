/* *** INPUT PINS SWITCHES */
/* *** SW_0*/
//P0.20
//B=00000000:00010000:00000000:00000000
//H=00:10:00:00 
#define SW_0 0x00100000

/* *** SW_1*/
//P0.10
//B=00000000:00010000:00000100:00000000
//H=00:00:04:00 
#define SW_1 0x00000400

/* *** SW_2 */
//P0.15
//B=00000000:00000000:10000000:00000000
//H=00:00:80:00 
#define SW_2 0x00008000

/* *** SW_LW */
//P0.11
//B=00000000:00000000:00001000:00000000
//H=00:00:08:00 
#define SW_LW 0x00000800

/* *** SW_RW */
//P0.23
//B=00000000:10000000:00000000:00000000
//H=00:80:00:00 
#define SW_RW 0x00800000





/* *** OUTPUT PINS LEDs */
/* *** LED*/
//P0.16
//B=00000000:00000001:00000000:00000000
//H=00:01:00:00 
#define P0_LED 0x00010000

/* *** LED_0 */
//P1.28
//B=00010000:00000000:00000000:00000000
//H=10:00:00:00 
#define P1_LED_0 0x10000000


/* *** LED_1*/
//P1.27
//B=00001000:00000000:00000000:00000000
//H=08:00:00:00 
#define P1_LED_1 0x08000000

/* *** LED_2*/
//P0.31
//B=10000000:00000000:00000000:00000000
//H=80:00:00:00 
#define P0_LED_2 0x80000000

/* *** LED_LW*/
//P1.29
//B=00100000:00000000:00000000:00000000
//H=20:00:00:00 
#define P1_LED_LW 0x20000000

/* *** LED_RW*/
//P0.2
//B=00000000:00000000:00000000:00000100
//H=00:00:00:04 
#define P0_LED_RW 0x00000004


#endif
